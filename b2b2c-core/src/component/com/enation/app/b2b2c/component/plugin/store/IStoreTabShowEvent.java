package com.enation.app.b2b2c.component.plugin.store;

import com.enation.app.b2b2c.core.store.model.Store;

/**
 * 店铺tab显示事件
 * @author Kanon
 *
 */
public interface IStoreTabShowEvent {

	/**
	 * 返回选项卡的名称
	 * @return
	 */
	public String getTabName();
	
	
	/**
	 * 返回排序
	 * @return
	 */
	public int getOrder();
	
	/**
	 * 返回修改选项卡页面
	 * @return
	 */
	public String getEditTabHtml(Store store);
	
	/**
	 * 返回添加Tab html页面
	 * @return
	 */
	public String getAddTabHtml();
}
